package people;

public class Player {
  int health;
  String name;
  
  public void setHealth(int heal) {
    health = heal;
    
  }
  public int getHealth() {
    return health;
    
  }
  public void setName(String nam) {
    name = nam;
    
  }
  public String getName() {
    return name;
  }
}
